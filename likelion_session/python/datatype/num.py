# 주석 = 메모

# 숫자형
# 정수 == int, 실수 == float

# 사칙연산 실습
num1 = int(input("여기에 첫번째 숫자를 입력하세요: "))
num2 = int(input("여기에 두번째 숫자를 입력하세요: "))

print(num1*num2)

# 나눗셈 - 몫 연산 & 나머지 연산
print("몫 연산 결과 : ", num1//num2)
print("나머지 연산 결과 : ", num1%num2)
print("일반적인 나눗셈 연산 결과 : ",num1/num2)
print("거듭제곱 연산 결과 : ", num1**num2)

# float 실습
float1 = float(input("첫번째 실수 입력: "))
float2 = float(input("두번째 실수 입력: "))

print("float1 + float2 = ", float1+float2)
print("float1 * float2 = ", float1/float2)
print("float1^float2 = ", float1**float2)